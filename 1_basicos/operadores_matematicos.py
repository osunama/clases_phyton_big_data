
numA = 10
numB = 5
#suma
print(5 + 5)
print(numA+numB)

#resta
print(6-9)
print(numA-numB)

#multiplicación
print(3*3)
print(numA*numA)

#división
print(54/9)
print(numA/numB)

#potencia
print(2**3)
print(numA**numB)

#division entera (sin decimales)
print(18//5)
print("Resultado sin decimales:", 18//5)

#operado modulo (devuelve el resto de una division)
print(18%5)

#incremento / decremento / asignación
numero = 2

numero += 2 #es lo mismo que numero= numero +2 = 4
numero -= 1 #es lo mismo que numero= numero 1 (como ahora el valor es 4) = 3
numero += 5 #es lo mismo que numero= numero +5 (como ahora el valor es 3) = 8
print(numero)