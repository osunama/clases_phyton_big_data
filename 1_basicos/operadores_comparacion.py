numA = 18
numB = 5

#cuando comparamos, el resultado siempre es booleano

#mayor que
resultado = numA > numB
print(resultado)

#mayor o igual que
resultado = numA >= numB
print(resultado)

#menor que
resultado = numA < numB
print(resultado)

#menor o igual que
resultado = numA <= numB
print(resultado)

#Igualdad

resultado = numA == numB
print(resultado)

#distinto
resultado = numA != numB
print(resultado)
